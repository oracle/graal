    /// Generates paste-ready source code for WhiteBox native methods that are not yet implemented.
    ///
    /// ```
    /// mx -p substratevm build
    /// mx -p substratevm vm -cp @com.oracle.svm.core com.oracle.svm.core.libjvm.WhiteBoxEntryPoints$SourceGenerator /path/to/labsjdk-ce
    /// ```
    @Platforms(Platform.HOSTED_ONLY.class)
    public static final class SourceGenerator {
        private static final String WHITEBOX_SOURCE = "jdk/test/whitebox/WhiteBox.java";

        private SourceGenerator() {
        }

        /// Compiles the WhiteBox source in the JDK repository passed through `args` and prints native entry-point stubs.
        public static void main(String[] args) throws IOException {
            if (args.length != 1) {
                throw new IllegalArgumentException("Usage: SourceGenerator <jdk-repository>");
            }
            Path baseDirectory = Path.of(args[0]).resolve("test/lib");
            Path sourceFile = baseDirectory.resolve(WHITEBOX_SOURCE);
            if (!Files.isRegularFile(sourceFile)) {
                throw new IOException("WhiteBox source file does not exist: " + sourceFile);
            }

            JavaCompiler compiler = ToolProvider.getSystemJavaCompiler();
            if (compiler == null) {
                throw new IllegalStateException("A JDK compiler is required");
            }
            Path outputDirectory = Files.createTempDirectory("whitebox-class-");
            try {
                try (StandardJavaFileManager fileManager = compiler.getStandardFileManager(null, null, null)) {
                    Iterable<? extends javax.tools.JavaFileObject> compilationUnits = fileManager.getJavaFileObjects(sourceFile.toFile());
                    List<String> options = List.of("--source-path", baseDirectory.toString(), "-d", outputDirectory.toString());
                    boolean compiled = compiler.getTask(new PrintWriter(System.out, true), fileManager, null, options, null, compilationUnits).call();
                    if (!compiled) {
                        throw new IllegalStateException("Could not compile " + sourceFile);
                    }
                }

                Path classFile = outputDirectory.resolve("jdk/test/whitebox/WhiteBox.class");
                ClassModel classModel = ClassFile.of().parse(Files.readAllBytes(classFile));
                Set<String> existingMethods = existingWhiteBoxMethods();
                List<MethodModel> methods = classModel.methods().stream() //
                                .filter(method -> method.flags().has(AccessFlag.NATIVE)) //
                                .filter(method -> !existingMethods.contains(method.methodName().stringValue())) //
                                .sorted(Comparator.comparing(m -> m.methodName().stringValue())) //
                                .toList();
                System.out.println();
                System.out.println("--- Entries to add to WHITE_BOX_NATIVE_METHODS initializer ---");
                System.out.println();
                for (var method : methods) {
                    System.out.println("                    method(\"" + method.methodName().stringValue() + "\", \"" + method.methodType().stringValue() + "\"),");
                }
                System.out.println();
                System.out.println("--- @CEntryPoint methods ---");
                System.out.println();
                for (var method : methods) {
                    printStub(method.methodName().stringValue(), method.methodType().stringValue());
                }
            } finally {
                deleteRecursively(outputDirectory);
            }
        }

        /// Finds WhiteBox native names that already have a C entry point.
        private static Set<String> existingWhiteBoxMethods() {
            String prefix = "Java_jdk_test_whitebox_WhiteBox_";
            Set<String> result = new HashSet<>();
            for (Method method : WhiteBoxEntryPoints.class.getDeclaredMethods()) {
                // Checkstyle: allow direct annotation access
                CEntryPoint entryPoint = method.getAnnotation(CEntryPoint.class);
                // Checkstyle: disallow direct annotation access
                if (entryPoint != null && entryPoint.name().startsWith(prefix)) {
                    result.add(entryPoint.name().substring(prefix.length()));
                }
            }
            return result;
        }

        /// Prints a placeholder C entry-point stub for a native method in the compiled WhiteBox class.
        private static void printStub(String methodName, String descriptor) {
            List<String> parameterTypes = parseParameterTypes(descriptor);
            String returnType = parseReturnType(descriptor);
            String exceptionHandler = exceptionHandler(returnType);

            System.out.println("    @CEntryPoint(exceptionHandler = " + exceptionHandler + ".class, name = \"Java_jdk_test_whitebox_WhiteBox_" + methodName +
                            "\", publishAs = Publish.NotPublished, include = LibJVMSupport.Enabled.class)");
            System.out.println("    @CEntryPointOptions(prologue = JNIEnvEnterFatalOnFailurePrologue.class)");
            StringBuilder declaration = new StringBuilder("    static ").append(returnType).append(' ').append(methodName).append(
                            "(@SuppressWarnings(\"unused\") JNIEnvironment env, @SuppressWarnings(\"unused\") JNIObjectHandle self");
            for (int i = 0; i < parameterTypes.size(); i++) {
                declaration.append(", @SuppressWarnings(\"unused\") ").append(parameterTypes.get(i)).append(" arg").append(i);
            }
            declaration.append(") {");
            System.out.println(declaration);
            System.out.println("        // TODO");
            System.out.println("    }");
            System.out.println();
        }

        /// Parses parameter types from a JVM method descriptor.
        private static List<String> parseParameterTypes(String descriptor) {
            List<String> result = new java.util.ArrayList<>();
            int index = 1;
            while (descriptor.charAt(index) != ')') {
                ParsedType type = parseType(descriptor, index);
                result.add(type.javaType());
                index = type.nextIndex();
            }
            return result;
        }

        /// Parses the return type from a JVM method descriptor.
        private static String parseReturnType(String descriptor) {
            int index = descriptor.indexOf(')') + 1;
            return parseType(descriptor, index).javaType();
        }

        /// Parses one JVM descriptor type and returns the next descriptor index.
        private static ParsedType parseType(String descriptor, int startIndex) {
            char kind = descriptor.charAt(startIndex);
            if (kind == '[' || kind == 'L') {
                int endIndex = startIndex;
                while (descriptor.charAt(endIndex) == '[') {
                    endIndex++;
                }
                if (descriptor.charAt(endIndex) == 'L') {
                    endIndex = descriptor.indexOf(';', endIndex);
                }
                return new ParsedType("JNIObjectHandle", endIndex + 1);
            }
            String javaType = switch (kind) {
                case 'Z' -> "boolean";
                case 'B' -> "byte";
                case 'C' -> "char";
                case 'S' -> "short";
                case 'I' -> "int";
                case 'J' -> "long";
                case 'F' -> "float";
                case 'D' -> "double";
                case 'V' -> "void";
                default -> throw new IllegalArgumentException("Unknown JVM descriptor type: " + kind);
            };
            return new ParsedType(javaType, startIndex + 1);
        }

        /// Selects the JNI exception handler for a generated return type.
        private static String exceptionHandler(String returnType) {
            return switch (returnType) {
                case "void" -> "JNIExceptionHandlerVoid";
                case "boolean" -> "JNIExceptionHandlerReturnFalse";
                case "int", "long" -> "JNIExceptionHandlerReturnZero";
                default -> "JNIExceptionHandlerReturnNullHandle";
            };
        }

        private record ParsedType(String javaType, int nextIndex) {
        }

        /// Deletes a temporary directory and its contents.
        private static void deleteRecursively(Path directory) throws IOException {
            try (var paths = Files.walk(directory)) {
                for (Path path : paths.sorted(Comparator.reverseOrder()).toList()) {
                    Files.deleteIfExists(path);
                }
            }
        }
    }
