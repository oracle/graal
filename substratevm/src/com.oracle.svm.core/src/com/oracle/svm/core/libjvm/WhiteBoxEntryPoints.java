    /// JNI linkage symbol for [#registerNatives].
    public static final String WHITEBOX_REGISTER_NATIVES_SYMBOL = "Java_jdk_test_whitebox_WhiteBox_registerNatives";

    /// Entry point address for [#registerNatives].
    public static final CEntryPointLiteral<?> WHITEBOX_REGISTER_NATIVES = CEntryPointLiteral.create(WhiteBoxEntryPoints.class, "registerNatives", JNIEnvironment.class, JNIObjectHandle.class);

    @CEntryPoint(exceptionHandler = JNIExceptionHandlerVoid.class, name = WHITEBOX_REGISTER_NATIVES_SYMBOL, publishAs = Publish.NotPublished, include = LibJVMSupport.Enabled.class)
    @CEntryPointOptions(prologue = JNIEnvEnterFatalOnFailurePrologue.class)
    static void registerNatives(@SuppressWarnings("unused") JNIEnvironment env, JNIObjectHandle clazz) {
        Class<?> whiteBoxClass = JNIObjectHandles.getObject(clazz);
        if (!SubstrateOptions.WhiteBoxAPI.getValue() || whiteBoxClass.getClassLoader() != null) {
            throw new UnsatisfiedLinkError("WhiteBox native methods are only available for the boot-loaded WhiteBox class when -XX:+WhiteBoxAPI is enabled");
        }
        DynamicHub declaringClass = DynamicHub.fromClass(JNIObjectHandles.getObject(clazz));
        WhiteBoxNativeMethod[] methods = WHITE_BOX_NATIVE_METHODS;
        for (int i = 0; i < methods.length; i++) {
            WhiteBoxNativeMethod method = methods[i];
            try {
                Support.registerNative(declaringClass, method.name(), method.signature(), i, method.entryPoint().getFunctionPointer(), true);
            } catch (NoSuchMethodError e) {
                // NoSuchMethodError is thrown when a method can't be found or a method is not native.
                // Ignore the exception since it is not preventing use of other WhiteBox methods.
                // This mimics HotSpot behavior.
                LogUtils.warning(e.toString());
            }
        }
    }

