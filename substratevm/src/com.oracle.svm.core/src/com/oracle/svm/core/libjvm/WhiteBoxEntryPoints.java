    /// Gets the descriptor for the runtime option named by `nameHandle`.
    ///
    /// If the option does not exist or is not of type `type`, null is returned
    /// *and* a warning is logged. HotSpot's WhiteBox functions for accessing
    /// options do not issue a warning. However, SVM does not
    /// have support for JEP 358 ("Helpful NullPointerExceptions"), so it can be
    /// hard to know whether an NPE is due to a missing option or an attempt
    /// to access an existing option with the wrong type.
    private static Descriptor getDescriptor(JNIObjectHandle nameHandle, Class<?> type) {
        String name = JNIObjectHandles.getObject(nameHandle);
        Descriptor descriptor = RuntimeOptions.getDescriptor(name);
        if (descriptor == null) {
            warn("Unknown option: " + name);
            return null;
        } else if (!isExpectedType(type, descriptor.valueType())) {
            warn("Expected " + ClassUtil.getUnqualifiedName(type) + " option, got " + ClassUtil.getUnqualifiedName(descriptor.valueType()) + " option: " + name);
            return null;
        }
        return descriptor;
    }

    /// Reports whether `actualType` is compatible with the WhiteBox accessor for `expectedType`.
    private static boolean isExpectedType(Class<?> expectedType, Class<?> actualType) {
        if (expectedType == Object.class) {
            return true;
        }
        if (expectedType == Number.class) {
            // Runtime option numbers use boxed primitive types.
            return actualType == Byte.class || actualType == Short.class || actualType == Integer.class || actualType == Long.class || actualType == Float.class || actualType == Double.class;
        }
        return expectedType == actualType;
    }

    /// Gets the current runtime option value for `name`.
    private static Object getRuntimeOption(String name) {
        try {
            return RuntimeOptions.get(name);
        } catch (RuntimeException e) {
            return null;
        }
    }

    /// Gets a runtime option value with a fallback for WhiteBox aliases.
    private static <T> T getOption(String name, Class<T> requiredType, T defaultValue) {
        Descriptor descriptor = RuntimeOptions.getDescriptor(name);
        if (descriptor == null) {
            if (defaultValue != null) {
                return defaultValue;
            }
            throw new RuntimeException("Unknown option: " + name);
        }

        if (!isExpectedType(requiredType, descriptor.valueType())) {
            throw new RuntimeException("Option " + name + " is of type " + descriptor.valueType() + ", not " + requiredType);
        }
        return RuntimeOptions.get(descriptor.name());
    }

    private static void warn(String msg) {
        Log.log().string("Substrate VM warning: [WhiteBox] ").string(msg).newline();
    }

    /// Gets the runtime option named by `nameHandle` if it has type `type`.
    private static JNIObjectHandle getRuntimeOption(JNIObjectHandle nameHandle, Class<?> type) {
        Descriptor descriptor = getDescriptor(nameHandle, type);
        if (descriptor == null) {
            return JNIObjectHandles.nullHandle();
        }
        Object value = getRuntimeOption(descriptor.name());
        return JNIObjectHandles.createLocal(value);
    }

    /// Gets the numeric runtime option named by `nameHandle` as a boxed `long`.
    private static JNIObjectHandle getLongRuntimeOption(JNIObjectHandle nameHandle) {
        Descriptor descriptor = getDescriptor(nameHandle, Number.class);
        if (descriptor == null) {
            return JNIObjectHandles.nullHandle();
        }
        Object value = getRuntimeOption(descriptor.name());
        return value instanceof Number number ? JNIObjectHandles.createLocal(number.longValue()) : JNIObjectHandles.nullHandle();
    }

    /// Sets the runtime option named by `nameHandle` to `value`.
    private static void setRuntimeOption(JNIObjectHandle nameHandle, Object value) {
        Class<?> type = value == null ? Object.class : value.getClass();
        Descriptor descriptor = getDescriptor(nameHandle, type);
        if (descriptor == null || value == null) {
            return;
        }
        Object convertedValue = convertValue(descriptor, value);
        if (convertedValue != null) {
            try {
                RuntimeOptions.set(descriptor.name(), convertedValue);
            } catch (RuntimeException e) {
                // WhiteBox setters silently ignore flags that cannot be updated in Crema.
            }
        }
    }

    /// Converts `value` to the Java type expected by `descriptor`.
    private static Object convertValue(Descriptor descriptor, Object value) {
        if (descriptor.valueType().isInstance(value)) {
            return value;
        }
        try {
            return descriptor.convertValue(value.toString());
        } catch (IllegalArgumentException e) {
            return null;
        }
    }

