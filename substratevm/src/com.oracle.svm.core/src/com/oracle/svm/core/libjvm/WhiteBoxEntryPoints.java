    @Platforms(Platform.HOSTED_ONLY.class)
    private static Method findMethod(String methodName) {
        Method result = null;
        for (Method m : WhiteBoxEntryPoints.class.getDeclaredMethods()) {
            if (m.getName().equals(methodName)) {
                VMError.guarantee(result == null, "multiple definitions found: %s and %s", result, m);
                result = m;
            }
        }
        VMError.guarantee(result != null, "could not find method %s in %s", methodName, WhiteBoxEntryPoints.class);
        return result;
    }

    /// Creates a WhiteBox native method descriptor and its managed JNI entry point.
    @Platforms(Platform.HOSTED_ONLY.class)
    private static WhiteBoxNativeMethod method(String name, String signature) {
        Method declaredMethod = findMethod(name);
        CEntryPointLiteral<?> entryPoint = CEntryPointLiteral.create(WhiteBoxEntryPoints.class, name, declaredMethod.getParameterTypes());
        return new WhiteBoxNativeMethod(name, signature, entryPoint);
    }

